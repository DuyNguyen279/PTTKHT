    private void setDataTable (List<item> list){
        model.setRowCount(0);
        for (item x : list){
            if (!x.isIs_delete()){
                model.addRow(new Object[]{
                    x.getItem_id(),
                    x.getItem_name(),
                    x.getItem_quantity(),
                    x.getItem_price()
                });
            }
        }
    }

