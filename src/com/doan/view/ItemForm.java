    private void delBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delBtnActionPerformed
        // TODO add your handling code here:
        int i = listItem.getSelectedRow();
        if (i == -1){
            JOptionPane.showMessageDialog(null, "Vui lòng chọn sản phẩm cần xóa");
            return;
        }
        int x = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa sản phẩm này không?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (x != JOptionPane.YES_OPTION){
            return;
        }
        String id = (String) listItem.getValueAt(i, 0);
        new delete().getInstance().deleteItem(id);
        JOptionPane.showMessageDialog(this, "Xóa sản phẩm thành công");
        setDataTable(new ItemDAO().getInstance().selectAll());
    }//GEN-LAST:event_delBtnActionPerformed

