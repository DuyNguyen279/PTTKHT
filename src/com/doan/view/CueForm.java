    private void addNewCueBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addNewCueBtnActionPerformed
        // TODO add your handling code here:
        if (txtNameCue.getText().isEmpty() || txtPriceCue.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int x = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn thêm cue mới không?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (x != JOptionPane.YES_OPTION) {
            return;
        }
        String id = txtIdCue.getText();
        String name = txtNameCue.getText();
        Double price = Double.parseDouble(txtPriceCue.getText());
        int quantity = Integer.parseInt(txtQuantityCue.getText());
        if (quantity < 0) {
            JOptionPane.showMessageDialog(null, "Số lượng không được âm!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        new addNew().getInstance().addNewCue(id, name, price, quantity);
        
        txtIdCue.setText("");
        txtNameCue.setText("");
        txtPriceCue.setText("");
        txtQuantityCue.setText("");

        JOptionPane.showMessageDialog(null, "Thêm gậy thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        addCue.setVisible(false);
        loadDataTable(new CueDAO().getInstance().selectAll());
    }//GEN-LAST:event_addNewCueBtnActionPerformed

