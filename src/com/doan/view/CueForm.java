    private void delteCueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delteCueActionPerformed
        // TODO add your handling code here:
        int i_row = listCue.getSelectedRow();
        if (i_row == -1) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn gậy cần xóa!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int x = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa gậy không?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (x != JOptionPane.YES_OPTION) {
            return;
        }
        String id = listCue.getValueAt(i_row, 0).toString();
        new delete().getInstance().deleteCue(id);
        JOptionPane.showMessageDialog(null, "Xóa gậy thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        loadDataTable(new CueDAO().getInstance().selectAll());
    }//GEN-LAST:event_delteCueActionPerformed

