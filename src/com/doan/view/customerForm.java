    private void delteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delteActionPerformed
        // TODO add your handling code here:
        int row = listCus.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn khách hàng cần xóa");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa khách hàng này không?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        String cusId = listCus.getValueAt(row, 0).toString();
        new delete().getInstance().deleteCustomer(cusId);
        JOptionPane.showMessageDialog(this, "Xóa khách hàng thành công");
        setDataTable(new customerDAO().getInstance().selectAll());
    }//GEN-LAST:event_delteActionPerformed

    private void UpdateCusBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateCusBtnActionPerformed
        // TODO add your handling code here:
        if (txtEditName.getText().isEmpty() || txtEditPhone.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn cập nhật khách hàng này không?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        String cusId = txtEditId.getText();
        String cusName = txtEditName.getText();
        String cusPhone = txtEditPhone.getText();
        if (cusPhone.length() != 10) {
            JOptionPane.showMessageDialog(null, "Số điện thoại không hợp lệ");
            return;
        }
        new edit().getInstance().editCustomer(cusId, cusName, cusPhone);
        JOptionPane.showMessageDialog(null, "Cập nhật khách hàng thành công");
        setDataTable(new customerDAO().getInstance().selectAll());
        editCus.setVisible(false);
    }//GEN-LAST:event_UpdateCusBtnActionPerformed

