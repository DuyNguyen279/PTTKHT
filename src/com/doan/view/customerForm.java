    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        int row = listCus.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn khách hàng cần chỉnh sửa");
            return;
        }
        txtEditId.setText(listCus.getValueAt(row, 0).toString());
        txtEditName.setText(listCus.getValueAt(row, 1).toString());
        txtEditPhone.setText(listCus.getValueAt(row, 2).toString());
        editCus.setLocationRelativeTo(this);
        editCus.setVisible(true);
    }//GEN-LAST:event_editActionPerformed

    public void setDataTable(List<customer> list) {
        model.setRowCount(0);
        for (customer customer : list) {
            if (!customer.isIs_delete()) {
                model.addRow(new Object[]{
                    customer.getCus_id(),
                    customer.getCus_name(),
                    customer.getCus_phone(),
                    customer.getPoint()
                });    
            }  
        }
    }

